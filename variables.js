// Variables are containers for storing data(storing data values).
// In JavaScript, A variable store a data value that can be changed later
// There are reserved keywords for declaring variables in JS.
// var, let, const
var a = 20;
var a = 40;
a = 60;
console.log(a);
// when we declare variables using 'var' keyword they can be Redeclarable and Reassignable.
let b = 50;
b = 60;
console.log(b);
// when we declare variables using 'let' keyword they can be Reassignable but not Redeclarable.
const c = 70;
c = 80;
console.log(c);
// when we declare variables using 'const' keyword they can not be Redclarable and Reassignable.
